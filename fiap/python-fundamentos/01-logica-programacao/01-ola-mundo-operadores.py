# Operadores Aritméticos
print("Operadores Aritméticos:")
print("10 + 5 =", 10 + 5)  # Soma
print("10 - 5 =", 10 - 5)  # Subtração
print("10 * 5 =", 10 * 5)  # Multiplicação
print("10 / 5 =", 10 / 5)  # Divisão
print("10 % 3 =", 10 % 3, " (Módulo - resto da divisão)")
print("10 ** 2 =", 10 ** 2," (Exponenciação)")
print("\n")  # Quebra de linha

#tipo de dados
print("Tipos de Dados:")
print("Tipo de soma:", type(10 + 5))
print("Tipo de subtração:", type(10 - 5))
print("Tipo de multiplicação:", type(10 * 5))
print("Tipo de divisão:", type(10 / 5))
print("Tipo de 'Hello World!':", type("Hello World!"))
print("Tipo de 10:", type(10))
print("Tipo de 3.14:", type(3.14))
print("Tipo de True:", type(bool(True)))
print("Tipo de False:", type(bool(False)))
print("Tipo de None:", type(None))
print("Tipo de [1, 2, 3]:", type([1, 2, 3]))
print("Tipo de (1, 2, 3):", type((1, 2, 3)))
print("\n")  # Quebra de linha

# Operadores de Atribuição
print("Operadores de Atribuição:")
x = 10
x += 5  # x = x + 5
print("Valor de x após += 5:", x)
x -= 3  # x = x - 3
print("Valor de x após -= 3:", x)
x *= 2  # x = x * 2
print("Valor de x após *= 2:", x)
x /= 4  # x = x / 4
print("Valor de x após /= 4:", x)
print("\n")  # Quebra de linha

# Operadores de Comparação
print("Operadores de Comparação:")
a = 10
b = 5
print("a == b:", a == b)  # Igual a
print("a != b:", a != b)  # Diferente de
print("a > b:", a > b)    # Maior que
print("a < b:", a < b)    # Menor que
print("a >= b:", a >= b)  # Maior ou igual a
print("a <= b:", a <= b)  # Menor ou igual a
print("\n")  # Quebra de linha

# Operadores Lógicos
print("Operadores Lógicos:")
c = True
d = False
print("c and d:", c and d)  # E lógico
print("c or d:", c or d)    # Ou lógico
print("not c:", not c)      # Negação lógica
print("not d:", not d)      # Negação lógica
print("\n")  # Quebra de linha

